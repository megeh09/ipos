    private void triggerButtonMouseExited(JButton button, Integer hoveredMenu) {
        if (!Objects.equals(hoveredMenu, menuActive)) {
            button.setBackground(MOUSE_EXITED_COLOR);
            button.setForeground(BUTTON_DEFAULT_COLOR);
        }
    }

    private void triggerChangeButton(JButton button) {
        // Change colors when another button is clicked.
        if (!Objects.equals(button, previousButton)) {
            previousButton.setBackground(MOUSE_EXITED_COLOR);
            previousButton.setForeground(BUTTON_DEFAULT_COLOR);
        }
    }

